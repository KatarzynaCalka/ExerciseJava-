package com.exerciseJava;

/*
Write a program that will display the 4 seasons of the next lines.
Solve this exercise in three ways.
*/

public class Exercise03 {
    public static void main(String[] args) {

        System.out.println("First method:");
        System.out.println("Spring");
        System.out.println("Summer");
        System.out.println("Autumn");
        System.out.println("Winter");

        System.out.println("\nSecond method:");
        System.out.println("Spring\nSummer\nAutumn\nWinter");

        System.out.println("\nThird method:");
        System.out.println("Spring\n" + "Summer\n" + "Autumn\n" + "Winter");
    }
}
